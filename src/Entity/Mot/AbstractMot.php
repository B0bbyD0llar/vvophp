<?php declare(strict_types=1);

namespace VVOphp\Entity\Mot;

abstract class AbstractMot
{
    private string $name;

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName(string $name): void
    {
        $this->name = $name;
    }

}